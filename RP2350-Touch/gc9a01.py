from machine import Pin
import framebuf
import time

class GC9A01:
    def __init__(self, spi, dc, cs, rst, bl, width=240, height=240):
        self.spi = spi
        self.dc = Pin(dc, Pin.OUT)
        self.cs = Pin(cs, Pin.OUT)
        self.rst = Pin(rst, Pin.OUT)
        self.bl = Pin(bl, Pin.OUT)
        self.width = width
        self.height = height
        
        self.buffer = bytearray(self.height * self.width * 2)
        self.fb = framebuf.FrameBuffer(self.buffer, self.width, self.height, framebuf.RGB565)
        self.init_display()

    def w_cmd(self, c):
        self.cs(0)
        self.dc(0)
        self.spi.write(bytearray([c]))
        self.cs(1)

    def w_data(self, d):
        self.cs(0)
        self.dc(1)
        if isinstance(d, int):
            self.spi.write(bytearray([d]))
        else:
            self.spi.write(d)
        self.cs(1)

    def init_display(self):
        self.bl(1)
        self.rst(1); time.sleep(0.1)
        self.rst(0); time.sleep(0.1)
        self.rst(1); time.sleep(0.1)

        cmds = [
            (0xEF, None), (0xEB, 0x14), (0xFE, None), (0xEF, None), (0xEB, 0x14),
            (0x84, 0x40), (0x85, 0xFF), (0x86, 0xFF), (0x87, 0xFF), (0x88, 0x0A),
            (0x89, 0x21), (0x8A, 0x00), (0x8B, 0x80), (0x8C, 0x01), (0x8D, 0x01),
            (0x8E, 0xFF), (0x8F, 0xFF), (0xB6, bytes([0x00, 0x20])),
            (0x36, 0x08), # 0x08 = BGR, 0x48 = RGB
            (0x3A, 0x05), (0x90, bytes([0x08, 0x08, 0x08, 0x08])), (0xBD, 0x06),
            (0xBC, 0x00), (0xFF, bytes([0x60, 0x01, 0x04])), (0xC3, 0x13), (0xC4, 0x13),
            (0xC9, 0x22), (0xBE, 0x11), (0xE1, bytes([0x10, 0x0E])),
            (0xDF, bytes([0x21, 0x0c, 0x02])), (0xF0, bytes([0x45, 0x09, 0x08, 0x08, 0x26, 0x2A])),
            (0xF1, bytes([0x43, 0x70, 0x72, 0x36, 0x37, 0x6F])), (0xF2, bytes([0x45, 0x09, 0x08, 0x08, 0x26, 0x2A])),
            (0xF3, bytes([0x43, 0x70, 0x72, 0x36, 0x37, 0x6F])), (0xED, bytes([0x1B, 0x0B])),
            (0xAE, 0x77), (0xCD, 0x63), (0x70, bytes([0x07, 0x07, 0x04, 0x0E, 0x0F, 0x09, 0x07, 0x08, 0x03])),
            (0xE8, 0x34), (0x62, bytes([0x18, 0x0D, 0x71, 0xED, 0x70, 0x70, 0x18, 0x0F, 0x71, 0xEF, 0x70, 0x70])),
            (0x63, bytes([0x18, 0x11, 0x71, 0xF1, 0x70, 0x70, 0x18, 0x13, 0x71, 0xF3, 0x70, 0x70])),
            (0x64, bytes([0x28, 0x29, 0xF1, 0x01, 0xF1, 0x00, 0x07])),
            (0x66, bytes([0x3C, 0x00, 0xCD, 0x67, 0x45, 0x45, 0x10, 0x00, 0x00, 0x00])),
            (0x67, bytes([0x00, 0x3C, 0x00, 0x00, 0x00, 0x01, 0x54, 0x10, 0x32, 0x98])),
            (0x74, bytes([0x10, 0x85, 0x80, 0x00, 0x00, 0x4E, 0x00])), (0x98, bytes([0x3e, 0x07])),
            (0x35, None), (0x21, None), (0x11, None)
        ]
        for cmd, data in cmds:
            self.w_cmd(cmd)
            if data is not None: self.w_data(data)
        time.sleep(0.12)
        self.w_cmd(0x29)

    def show(self):
        """Sendet den kompletten Puffer (inkl. geblittetem Bild) ans Display."""
        # Fenster auf das gesamte Display setzen
        self.w_cmd(0x2A); self.w_data(0); self.w_data(0); self.w_data(0); self.w_data(239)
        self.w_cmd(0x2B); self.w_data(0); self.w_data(0); self.w_data(0); self.w_data(239)
        self.w_cmd(0x2C)
        
        self.cs(0)
        self.dc(1)
        self.spi.write(self.buffer) # High-Speed Burst des gesamten RAMs
        self.cs(1)
